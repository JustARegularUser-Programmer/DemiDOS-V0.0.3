int cli_x = 20;
int cli_y = 40;

static inline uint8_t inb(uint16_t port) {
    uint8_t ret;
    __asm__ volatile ("inb %1, %0" : "=a"(ret) : "Nd"(port));
    return ret;
}

void clear_screen() {
    for (int i = 0; i < SCREEN_WIDTH * SCREEN_HEIGHT; i++) {
        video_memory[i] = 0x000000;
    }
}

void draw_c(int x, int y, char c, uint32_t color) {
    for (int row = 0; row < 8; row++) {
        uint8_t bits = font_8x8[(unsigned char)c][row];

        // Перебираем 8 пикселей в строке (слева направо)
        for (int col = 0; col < 8; col++) {
            // ИСПРАВЛЕНО: проверяем биты слева направо (начиная со старшего бита 0x80)
            if (bits & (0x80 >> col)) {
                // Простая проверка границ экрана 1024x768 (БЕЗ переворотов Y!)
                if ((x + col) < SCREEN_WIDTH && (y + row) < SCREEN_HEIGHT) {
                    video_memory[(y + row) * SCREEN_WIDTH + (x + col)] = color;
                }
            }
        }
    }
}

void draw_s(int x, int y, const char* str, uint32_t color) {
    while (*str) {
        draw_c(x, y, *str, color);
        x += 8;
        str++;
    }
}

char get_ascii(void) {
    while (1) {
        uint8_t status = inb(0x64);
        if (status & 1) {
            if ((status & 0x20) == 0) {
                uint8_t scancode = inb(0x60);
                if (status < 0x80) {
                    if (scancode < sizeof(scancode_to_ascii)) {
                        return scancode_to_ascii[scancode];
                    }
                }
            }
        }
    }
}

int strncmp(const char* s1, const char* s2, uint32_t len) {
    for (uint32_t i = 0; i < len; i++) {
        if (s1[i] != s2[i]) {
            return 0;
        }
    }
    return 1;
}

void start_demidos_rec(void) {
    clear_screen();
    draw_s(20, 100, "DemiDOS RECOVERY> ", 0x00FF00);
    cli_x = 20 + (18 * 8);
    cli_y = 100;
    char input_buffer[64];
    int input_idx = 0;
    for (int i = 0; i < 64; i++) input_buffer[i] = 0;
    while (1) {
        char key = get_ascii();
        if (key == '\n') {
            input_buffer[input_idx] = '\0';

            if (input_buffer[0] == 's' && input_buffer[1] == 't') {
                cli_y += 12;
                draw_s(20, cli_y, "Kernel: OK Memory: OK MBR: OK", 0x00FF00);
            } else {
                cli_y += 12;
                draw_s(20, cli_y, "Unknown command!", 0x00FF00);
            }
            for (int i = 0; i < 64; i++) input_buffer[i] = 0;
            input_idx = 0;
            cli_y += 16;
            draw_s(20, cli_y, "DemiDOS RECOVERY> ", 0x00FF00);
            cli_x = 20 + (18 * 8);
        } else if (key >= ' ' && key <= '~') {
            if (cli_x < SCREEN_WIDTH - 20) {
                input_buffer[input_idx++] = key;
                input_idx++;
                draw_c(cli_x, cli_y, key, 0xFFFFFF);
                cli_x += 8;
            }
        } else if (key == '\b') {
            if (cli_x > 168) {
                cli_x -= 8;
                for (int row = 0; row < 8; row++) {
                    for (int col = 0; col < 8; col++) {
                        video_memory[(cli_y + row) * SCREEN_WIDTH + (cli_x + col)] = 0x000000;
                    }
                }
            }
        }
    }
}
